package reversi.model;

/**
 * Enum возможных игровых параметров
 */
public enum GameSetting {
    /**
     * Режим игры против игрока
     */
    PLAYER,
    /**
     * Режим игры против компьютера в легком режиме
     */
    EASY,
    /**
     * Режим игры против компьютера в сложном роежиме
     */
    HARD,
}
